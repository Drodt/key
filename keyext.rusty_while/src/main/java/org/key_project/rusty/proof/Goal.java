package org.key_project.rusty.proof;

public final class Goal {
    private Node node;

    /**
     * creates a new goal referencing the given node
     */
    public Goal(Node node) {
        this.node = node;
    }

    public Node getNode() {
        return node;
    }

    public void setNode(Node node) {
        this.node = node;
    }
}
